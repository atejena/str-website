// Helper functions
export function getBlogPostById(id: string): BlogPost | undefined {
  return blogPosts.find((p) => p.id === id);
}

export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find((p) => p.slug === slug);
}

export function getPublishedPosts(): BlogPost[] {
  return blogPosts
    .filter((p) => p.published)
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
}

export function getFeaturedPosts(limit?: number): BlogPost[] {
  const featured = blogPosts
    .filter((p) => p.published && p.featured)
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
  return limit ? featured.slice(0, limit) : featured;
}

export function getPostsByCategory(category: BlogCategory): BlogPost[] {
  return blogPosts
    .filter((p) => p.published && p.category === category)
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime());
}

export function getRelatedPosts(post: BlogPost, limit: number = 3): BlogPost[] {
  return blogPosts
    .filter(
      (p) =>
        p.published &&
        p.id !== post.id &&
        (p.category === post.category || p.tags.some((tag) => post.tags.includes(tag)))
    )
    .sort((a, b) => new Date(b.publishDate).getTime() - new Date(a.publishDate).getTime())
    .slice(0, limit);
}

export function getBlogCategories(): BlogCategory[] {
  return ['Workout Tips', 'Nutrition', 'Member Spotlights', 'Gym News', 'Recovery', 'Motivation'];
}
